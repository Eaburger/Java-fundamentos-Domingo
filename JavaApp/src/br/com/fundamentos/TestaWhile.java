package br.com.fundamentos;

public class TestaWhile {
	public static void main(String[] args) {
		
		int contador = 2;
		
		
		while (contador >=0 ) {
			
			System.out.println("Contador --> " + contador);
			contador --;
			
			
		}
		
		System.out.println("Valor Final Contador " + contador);
		
		
		
	}

}
